const LOOP_BY_ID = new Map(LOOPS.map((l) => [l.id, l]));

export function getLoop(id: string): Loop | undefined {
  return LOOP_BY_ID.get(id);
}

/** Build the nested tree rooted at the CEO loop. */
export function buildLoopTree(): LoopTreeNode[] {
  const byParent = new Map<string | null, Loop[]>();
  for (const loop of LOOPS) {
    const key = loop.parentLoopId;
    const arr = byParent.get(key) ?? [];
    arr.push(loop);
    byParent.set(key, arr);
  }
  const build = (loop: Loop): LoopTreeNode => ({
    loop,
    children: (byParent.get(loop.id) ?? []).map(build),
  });
  return (byParent.get(null) ?? []).map(build);
}
